package com.syntax.class01;

public class PrimitiveDataTypes {
	public static void main(String[] arrgs) {

		byte variableOne = 10;

		short variableTwo = 100;

		int variaableThree = 1000;

		long variableFour = 10000000;

		// to store decimal numbers

		float variableFive = 12.99f;

		double anyName = 132.89;

		// to store single character

		char singleValue = 'F';
		char anotherVariable = '%';

		// to store boolean values

		boolean hungry = true;
		boolean thirsty = false;

		System.out.println(variableFour);

	}

}
